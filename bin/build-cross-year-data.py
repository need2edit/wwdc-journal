#!/usr/bin/env python3
"""
Extract three cross-year datasets from per-year session metadata:

- data/topic-prevalence.json — { topic: { year: count, ... }, ... }
- data/speaker-index.json    — [ { name, total, years: [year], sessions: [{year,id,title,topic}] }, ... ]
- data/glossary.json         — [ { term, count, years_seen: [year], category, sample_session }, ... ]

The glossary is generated by extracting CamelCase API names + known framework
mentions from session titles, descriptions, and gem text — filtered to terms
that appear in at least 3 sessions across at least 2 years.

Usage:
    bin/build-cross-year-data.py
"""
import json
import re
import sys
from collections import Counter, defaultdict
from pathlib import Path

PROJECT_ROOT = Path(__file__).parent.parent.resolve()
DATA_DIR = PROJECT_ROOT / "data"


def load_year(year: int):
    p = DATA_DIR / f"wwdc{year}-sessions.json"
    if not p.exists():
        return []
    return json.loads(p.read_text())


def build_topic_prevalence():
    topics = defaultdict(lambda: defaultdict(int))
    year_totals = defaultdict(int)
    for year in range(2014, 2026):
        for s in load_year(year):
            topic = s.get("topic", "Unknown")
            topics[topic][year] += 1
            year_totals[year] += 1
    out = {
        "topics": {
            t: {str(y): n for y, n in sorted(years.items())}
            for t, years in sorted(topics.items())
        },
        "year_totals": {str(y): n for y, n in sorted(year_totals.items())},
        "years": list(range(2014, 2026)),
    }
    (DATA_DIR / "topic-prevalence.json").write_text(json.dumps(out, indent=2))
    print(f"  Topic prevalence: {len(out['topics'])} topics × {len(out['years'])} years")
    print(f"    {DATA_DIR / 'topic-prevalence.json'}")


def build_speaker_index():
    """Group sessions by speaker. A 'thread' speaker = appears in 3+ years."""
    by_speaker = defaultdict(list)
    for year in range(2014, 2026):
        for s in load_year(year):
            for name in s.get("speakers", []):
                if not name or "&" in name or "+" in name:
                    continue
                by_speaker[name].append({
                    "year": year,
                    "id": s.get("eventContentId", ""),
                    "title": s.get("title", ""),
                    "topic": s.get("topic", ""),
                })

    speakers = []
    for name, sessions in by_speaker.items():
        years = sorted({s["year"] for s in sessions})
        # Most-frequent topic for this speaker
        topic_counts = Counter(s["topic"] for s in sessions if s["topic"])
        primary_topic = topic_counts.most_common(1)[0][0] if topic_counts else ""
        speakers.append({
            "name": name,
            "total": len(sessions),
            "years": years,
            "year_count": len(years),
            "primary_topic": primary_topic,
            "sessions": sorted(sessions, key=lambda x: (x["year"], x["id"])),
        })
    speakers.sort(key=lambda x: (-x["year_count"], -x["total"], x["name"]))
    (DATA_DIR / "speaker-index.json").write_text(json.dumps(speakers, indent=2))

    threads = [s for s in speakers if s["year_count"] >= 3]
    print(f"  Speakers: {len(speakers)} unique ({len(threads)} appearing in 3+ years)")
    top5 = ", ".join(f"{s['name']} ({s['year_count']}y)" for s in speakers[:5])
    print(f"    Top 5 by years: {top5}")
    print(f"    {DATA_DIR / 'speaker-index.json'}")


